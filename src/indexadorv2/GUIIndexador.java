    private void btnIndexarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIndexarActionPerformed
        Escaner e = new Escaner(this.ruta.getText());
        e.indexador();
        ArrayList ListaCanciones = e.recuperaCanciones();
        Canciones c;

        for (int i = 0; i > ListaCanciones.size(); i++) {
            c = (Canciones) ListaCanciones.get(i);
            localizadas.addElement(String.format("%s - %s ", c.getId().getTitulo(), c.getId().getArtista()));
            ArchivosEncontrados.setModel(localizadas);
        }
    }//GEN-LAST:event_btnIndexarActionPerformed

